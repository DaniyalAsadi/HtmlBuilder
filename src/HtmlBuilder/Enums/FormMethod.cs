namespace HtmlBuilder.Enums;

/// <summary>
/// Specifies the HTTP method to use when submitting the form.
/// </summary>
public enum FormMethod
{
    /// <summary>
    /// The GET method sends form data in the URL.
    /// </summary>
    Get,

    /// <summary>
    /// The POST method sends form data in the request body.
    /// </summary>
    Post
}

